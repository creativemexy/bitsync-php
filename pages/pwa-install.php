<script>
// Check if PWA is installable
if ('serviceWorker' in navigator) {
    // Show install prompt if available
    window.addEventListener('beforeinstallprompt', (e) => {
        e.preventDefault();
        
        // Create install button
        const installButton = document.createElement('button');
        installButton.textContent = 'Install Now';
        installButton.className = 'bg-green-600 text-white px-6 py-3 rounded-xl font-semibold hover:bg-green-700 transition-colors';
        installButton.onclick = () => {
            e.prompt();
            e.userChoice.then((choiceResult) => {
                if (choiceResult.outcome === 'accepted') {
                    console.log('User accepted the install prompt');
                }
            });
        };
        
        // Add to page
        const ctaSection = document.querySelector('.text-center .bg-gradient-to-r');
        if (ctaSection) {
            ctaSection.appendChild(installButton);
        }
    });
}

// Check if already installed
if (window.matchMedia('(display-mode: standalone)').matches) {
    console.log('App is already installed');
}
</script> 